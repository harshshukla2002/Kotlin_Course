package com.example.kotlinbasics

fun main() {

    // we can declare variable like this
    val myChar: Char = 'a'
    println(myChar)

    // we can define unicode like this
    val myUnicode = '\u003C'
    println(myUnicode)

    // we can add string variables like this
    val myStr: String = "Harsh"
    println(myStr)

    // strings also have some function we can use this using variable name and then dot
    println(myStr.uppercase())
}
